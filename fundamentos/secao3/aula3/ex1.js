let saldo = 500;


function adicioneUmValor (valor) {
  return valor + saldo;
   
};

function subtraiaUmValor (valor) {
    return saldo - valor;
};

function dividaUmValor (valor) {
    return saldo / valor;
};

function multipliqueUmValor (valor) {
    return valor * saldo;
};





console.log(adicioneUmValor(300));
console.log(subtraiaUmValor(200));
console.log(dividaUmValor(2));
console.log(multipliqueUmValor(5));



